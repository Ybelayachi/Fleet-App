-- DB2 schema script for fleet backend
CREATE TABLE APP_USER (
  USER_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  EMAIL VARCHAR(255) NOT NULL UNIQUE,
  PASSWORD VARCHAR(255) NOT NULL,
  FIRST_NAME VARCHAR(100),
  LAST_NAME VARCHAR(100),
  ROLE VARCHAR(50),
  ACTIVE SMALLINT DEFAULT 1
);

CREATE TABLE VEHICLE (
  VEHICLE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  VIN VARCHAR(255) NOT NULL UNIQUE,
  BRAND VARCHAR(100),
  MODEL VARCHAR(100),
  LICENSE_PLATE VARCHAR(50),
  IN_SERVICE_DATE DATE,
  ACTIVE SMALLINT DEFAULT 1
);

CREATE TABLE VEHICLE_ASSIGNMENT (
  ASSIGNMENT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  USER_ID BIGINT NOT NULL,
  VEHICLE_ID BIGINT NOT NULL,
  START_DATE DATE,
  END_DATE DATE,
  FOREIGN KEY (USER_ID) REFERENCES APP_USER(USER_ID),
  FOREIGN KEY (VEHICLE_ID) REFERENCES VEHICLE(VEHICLE_ID)
);

CREATE TABLE MONTHLY_MILEAGE (
  MILEAGE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  VEHICLE_ID BIGINT NOT NULL,
  YEAR INTEGER NOT NULL,
  MONTH INTEGER NOT NULL,
  MILEAGE BIGINT NOT NULL,
  DECLARED_AT TIMESTAMP,
  DECLARED_BY BIGINT,
  LAST_MODIFIED_AT TIMESTAMP,
  LAST_MODIFIED_BY BIGINT,
  FOREIGN KEY (VEHICLE_ID) REFERENCES VEHICLE(VEHICLE_ID),
  FOREIGN KEY (DECLARED_BY) REFERENCES APP_USER(USER_ID),
  FOREIGN KEY (LAST_MODIFIED_BY) REFERENCES APP_USER(USER_ID),
  CONSTRAINT UNQ_VEHICLE_YEAR_MONTH UNIQUE (VEHICLE_ID, YEAR, MONTH)
);

CREATE TABLE MONTHLY_MILEAGE_HISTORY (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MILEAGE_ID BIGINT NOT NULL,
  PREVIOUS_MILEAGE BIGINT,
  NEW_MILEAGE BIGINT,
  CHANGED_AT TIMESTAMP,
  CHANGED_BY BIGINT,
  FOREIGN KEY (MILEAGE_ID) REFERENCES MONTHLY_MILEAGE(MILEAGE_ID),
  FOREIGN KEY (CHANGED_BY) REFERENCES APP_USER(USER_ID)
);
